int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb < 0)
	{
		return (0);
	}
	if (nb == 1)
	{
		return (1);
	}
	while (x != nb)
	{
		if (x * x == nb)
		{
			return (x);
		}
		x++;
	}
	return (0);
}
